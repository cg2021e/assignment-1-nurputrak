function makePlane() {
    let obj = {};
    obj.vertices = [
        -1.000000, 000000, 1.000000, 0.000000, 1.000000, -0.000000, 2,0,32,  //2,0,32 is rgb for #020020
        1.000000, 000000, -1.000000, 0.000000, 1.000000, -0.000000, 2,0,32,
        -1.000000, 000000, -1.000000, 0.000000, 1.000000, -0.000000, 2,0,32,

        1.000000, 000000, 1.000000, 0.000000, 1.000000, -0.000000, 2,0,32,
    ];

    obj.indices = [
        0, 1, 2,
        0, 3, 1,

    ];

    obj.vertices = obj.vertices.map((v, i) => {
        if (i % 9 >= 3) {
            return v / 255;
        }
        return v;
    });

    return obj;
}